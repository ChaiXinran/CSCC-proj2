/**
 * Generates a self-contained AgentJS benchmark runner for a single JetStream test.
 * Unlike prepare-jetstream2.mjs, this bypasses the full JetStream driver and generates
 * a simple synchronous runner that:
 *   1. Embeds all benchmark JS files
 *   2. Executes them to define `class Benchmark`
 *   3. Calls `new Benchmark().runIteration()` N times
 *   4. Prints timing results
 *
 * Usage: node scripts/prepare-simple-benchmark.mjs <JetStreamRoot> <testName> [iterations] [output]
 */
import fs from "node:fs";
import path from "node:path";
import vm from "node:vm";

const root = path.resolve(process.argv[2] ?? "JetStream");
const testName = process.argv[3];
const iterationCount = Number(process.argv[4] ?? 3);
const output = path.resolve(
    process.argv[5] ?? `benchmarks/generated/${testName}-simple.js`,
);

if (!testName) {
    throw new Error(
        "usage: node scripts/prepare-simple-benchmark.mjs <root> <test> [iterations] [output]",
    );
}

// Discover benchmark files from JetStreamDriver
const driverSource = fs.readFileSync(path.join(root, "JetStreamDriver.js"), "utf8");
const discovery = {
    console,
    isInBrowser: false,
    RAMification: false,
    testIterationCount: undefined,
    testList: testName,
    readFile: () => "",
    runString: () => ({ print() {}, loadString() {} }),
    print() {},
    setTimeout,
    clearTimeout,
    Promise,
    Date,
    Math,
    Symbol,
    Map,
    Blob,
    JetStreamParams: {
        prefetchResources: false,
        forceGC: false,
        dumpJSONResults: false,
        testIterationCountMap: {},
        testWorstCaseCountMap: {},
        testList: testName,
    },
};
discovery.globalThis = discovery;
vm.createContext(discovery);
vm.runInContext(driverSource, discovery, { filename: "JetStreamDriver.js" });

const benchmarks = discovery.JetStream.benchmarks;
if (benchmarks.length !== 1) {
    throw new Error(`expected one benchmark, discovered ${benchmarks.length}`);
}

const benchmark = benchmarks[0];
const benchmarkFiles = benchmark._files ?? benchmark.files ?? [];

if (!benchmarkFiles.length) {
    throw new Error(`no files found for benchmark '${testName}'`);
}

// Read and embed all benchmark files
const fileContents = benchmarkFiles.map((relative) => {
    const normalized = relative.replaceAll("\\", "/");
    return {
        path: normalized,
        content: fs.readFileSync(path.resolve(root, relative), "utf8"),
    };
});

// Generate a minimal runner
const embedded = fileContents
    .map(({ path: p, content }) => `// === ${p} ===\n${content}`)
    .join("\n\n");

const runner = `
// AgentJS simple benchmark runner for: ${testName}
// Generated by scripts/prepare-simple-benchmark.mjs
// Iterations: ${iterationCount}

var console = { log: (...args) => print(...args), warn: (...args) => print(...args), error: (...args) => print(...args) };
var performance = { now: function() { return Date.now(); } };
// CommonJS shim: let UMD modules export to module.exports
var module = { exports: {} };
// Browser shim: let UMD modules that export to window work too
var window = globalThis;

(function() {
${embedded}

    // Create the benchmark instance (the embedded file must define 'class Benchmark')
    var bench = new Benchmark();
    var results = [];
    var iterations = ${iterationCount};

    for (var i = 0; i < iterations; i++) {
        var start = Date.now();
        bench.runIteration();
        var elapsed = Date.now() - start;
        results.push(elapsed);
        print("${testName} iteration " + (i + 1) + "/" + iterations + ": " + elapsed + "ms");
    }

    var total = results.reduce(function(a, b) { return a + b; }, 0);
    var avg = Math.round(total / results.length);
    print("${testName} avg: " + avg + "ms  total: " + total + "ms  iterations: " + iterations);
})();
`;

fs.mkdirSync(path.dirname(output), { recursive: true });
fs.writeFileSync(output, runner, "utf8");

console.log(
    JSON.stringify(
        {
            test: testName,
            iterations: iterationCount,
            files: benchmarkFiles,
            output,
        },
        null,
        2,
    ),
);
