#!/usr/bin/env python3
"""
Run QuickJS' bundled JavaScript microbenchmarks with AgentJS or another engine.

This script does not build or execute qjs. It reuses quickjs/tests/microbench.js
as a benchmark workload and injects scriptArgs through a temporary wrapper file
because AgentJS' `run` command does not pass argv through to JavaScript yet.
"""

from __future__ import annotations

import argparse
import json
import os
import re
import subprocess
import sys
import tempfile
import time
from dataclasses import asdict, dataclass
from pathlib import Path


ROOT = Path(__file__).resolve().parents[2]
DEFAULT_SOURCE = ROOT / "quickjs" / "tests" / "microbench.js"
DEFAULT_RESULTS = ROOT / "benchmarks" / "quickjs" / "results"
DEFAULT_BENCHES = [
    "empty_loop",
    "prop_read",
    "prop_write",
    "array_read",
    "array_write",
    "arguments_read",
    "global_read",
    "global_func_call",
    "func_call",
    "func_closure_call",
    "int_arith",
    "float_arith",
    "string_length",
    "int_to_string",
    "float_to_string",
]


@dataclass
class BenchRow:
    test: str
    n: str
    time_ns: float | None
    raw: str


@dataclass
class RunReport:
    label: str
    engine: list[str]
    source: str
    benches: list[str]
    status: str
    returncode: int | None
    elapsed_ms: float
    rows: list[BenchRow]
    stdout: str
    stderr: str


def default_agentjs() -> Path:
    suffix = ".exe" if os.name == "nt" else ""
    release = ROOT / "target" / "release" / f"agentjs{suffix}"
    debug = ROOT / "target" / "debug" / f"agentjs{suffix}"
    if release.exists():
        return release
    return debug


def split_csv(value: str | None) -> list[str]:
    if not value:
        return []
    return [part.strip() for part in value.split(",") if part.strip()]


def js_string(value: str) -> str:
    return json.dumps(value, ensure_ascii=False)


def make_wrapper(source_path: Path, benches: list[str], sort_array_size: int | None) -> Path:
    source = source_path.read_text(encoding="utf-8")
    script_args = [source_path.name]
    for bench in benches:
        script_args.append(bench)
    if sort_array_size is not None:
        script_args.extend(["-n", str(sort_array_size)])

    prefix = (
        "// Generated by benchmarks/quickjs/run_microbench.py\n"
        "var scriptArgs = ["
        + ", ".join(js_string(arg) for arg in script_args)
        + "];\n"
    )
    fd, name = tempfile.mkstemp(prefix="agentjs-quickjs-microbench-", suffix=".js")
    path = Path(name)
    with os.fdopen(fd, "w", encoding="utf-8", newline="\n") as handle:
        handle.write(prefix)
        handle.write(source)
    return path


def run_engine(command: list[str], timeout: int) -> tuple[int | None, str, str, float, bool]:
    started = time.perf_counter()
    try:
        proc = subprocess.run(
            command,
            cwd=ROOT,
            stdout=subprocess.PIPE,
            stderr=subprocess.PIPE,
            text=True,
            encoding="utf-8",
            errors="replace",
            timeout=timeout,
        )
        elapsed_ms = round((time.perf_counter() - started) * 1000, 1)
        return proc.returncode, proc.stdout, proc.stderr, elapsed_ms, False
    except subprocess.TimeoutExpired as error:
        elapsed_ms = round((time.perf_counter() - started) * 1000, 1)
        return (
            None,
            error.stdout or "",
            error.stderr or "",
            elapsed_ms,
            True,
        )


def parse_rows(stdout: str) -> list[BenchRow]:
    rows: list[BenchRow] = []
    for line in stdout.splitlines():
        stripped = line.strip()
        if not stripped or stripped.startswith("TEST "):
            continue
        if stripped.startswith("total"):
            continue
        match = re.match(r"^([A-Za-z0-9_]+)\s+([0-9.eE+-]+)\s+([0-9.eE+-]+)", stripped)
        if not match:
            continue
        rows.append(
            BenchRow(
                test=match.group(1),
                n=match.group(2),
                time_ns=float(match.group(3)),
                raw=line,
            )
        )
    return rows


def write_markdown(path: Path, report: RunReport) -> None:
    lines: list[str] = []
    lines.append("# QuickJS Microbench")
    lines.append("")
    lines.append(f"- Label: `{report.label}`")
    lines.append(f"- Engine: `{' '.join(report.engine)}`")
    lines.append(f"- Source: `{report.source}`")
    lines.append(f"- Status: `{report.status}`")
    lines.append(f"- Elapsed: `{report.elapsed_ms:.1f}ms`")
    lines.append("")
    lines.append("| Test | N | Time ns |")
    lines.append("|:---|---:|---:|")
    for row in report.rows:
        value = "" if row.time_ns is None else f"{row.time_ns:.2f}"
        lines.append(f"| {row.test} | {row.n} | {value} |")
    lines.append("")
    lines.append("## Raw Output")
    lines.append("")
    lines.append("```text")
    lines.append(report.stdout.rstrip())
    lines.append("```")
    if report.stderr.strip():
        lines.append("")
        lines.append("## Stderr")
        lines.append("")
        lines.append("```text")
        lines.append(report.stderr.rstrip())
        lines.append("```")
    path.write_text("\n".join(lines) + "\n", encoding="utf-8")


def main() -> int:
    parser = argparse.ArgumentParser(description="Run QuickJS tests/microbench.js as an AgentJS benchmark.")
    parser.add_argument("--source", default=str(DEFAULT_SOURCE), help="path to QuickJS microbench.js")
    parser.add_argument("--engine", default=str(default_agentjs()), help="engine executable")
    parser.add_argument(
        "--subcommand",
        default="jetstream",
        help="engine subcommand before the JS file; use '' for node",
    )
    parser.add_argument("--label", default="agentjs-quickjs-microbench", help="report label")
    parser.add_argument(
        "--bench",
        default=",".join(DEFAULT_BENCHES),
        help="comma-separated benchmark name prefixes; use 'all' for full microbench",
    )
    parser.add_argument("--timeout", type=int, default=120, help="timeout in seconds")
    parser.add_argument("--sort-array-size", type=int, default=None, help="optional microbench sort -n value")
    parser.add_argument("--out-json", default=None, help="JSON report path")
    parser.add_argument("--out-md", default=None, help="Markdown report path")
    parser.add_argument("--keep-wrapper", action="store_true", help="do not delete generated wrapper")
    parser.add_argument("--dry-run", action="store_true", help="print command and generated wrapper path")
    args = parser.parse_args()

    source = Path(args.source)
    if not source.exists():
        raise SystemExit(f"QuickJS microbench source not found: {source}")

    benches = [] if args.bench == "all" else split_csv(args.bench)
    wrapper = make_wrapper(source, benches, args.sort_array_size)
    command = [args.engine]
    if args.subcommand:
        command.append(args.subcommand)
    command.append(str(wrapper))

    if args.dry_run:
        print(subprocess.list2cmdline(command))
        print(f"wrapper: {wrapper}")
        if not args.keep_wrapper:
            try:
                wrapper.unlink()
            except OSError:
                pass
        return 0

    try:
        returncode, stdout, stderr, elapsed_ms, timed_out = run_engine(command, args.timeout)
    finally:
        if not args.keep_wrapper:
            try:
                wrapper.unlink()
            except OSError:
                pass

    rows = parse_rows(stdout)
    if timed_out:
        status = "timeout"
    elif returncode != 0:
        status = "runtime-error"
    elif not rows:
        status = "no-results"
    else:
        status = "pass"

    DEFAULT_RESULTS.mkdir(parents=True, exist_ok=True)
    out_json = Path(args.out_json) if args.out_json else DEFAULT_RESULTS / f"{args.label}.json"
    out_md = Path(args.out_md) if args.out_md else DEFAULT_RESULTS / f"{args.label}.md"

    report = RunReport(
        label=args.label,
        engine=command[:-1],
        source=str(source),
        benches=benches or ["all"],
        status=status,
        returncode=returncode,
        elapsed_ms=elapsed_ms,
        rows=rows,
        stdout=stdout,
        stderr=stderr,
    )
    out_json.write_text(
        json.dumps(asdict(report), ensure_ascii=False, indent=2) + "\n",
        encoding="utf-8",
    )
    write_markdown(out_md, report)

    print(f"RESULT {args.label}: {status}, rows={len(rows)}, elapsed={elapsed_ms:.1f}ms")
    print(f"JSON -> {out_json}")
    print(f"MD   -> {out_md}")
    return 0 if status == "pass" else 1


if __name__ == "__main__":
    raise SystemExit(main())
